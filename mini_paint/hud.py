class Hud:
    def __init__(self, renderer: Renderer) -> None:
        self.renderer = renderer
        self.scale = 0.09
        self.line_height = 0.95

    def draw(self, viewport, lines: list[str]) -> None:
        import numpy as np
        from OpenGL.GL import glUniform3f, glUniformMatrix4fv

        start_x = viewport.world_left + 0.35
        start_y = viewport.world_top - 0.55
        identity = np.eye(4, dtype=np.float32)
        glUniformMatrix4fv(self.renderer.u_model, 1, False, identity)
        glUniform3f(self.renderer.u_color, 0.92, 0.94, 0.98)

        for line_index, line in enumerate(lines):
            y = start_y - line_index * self.line_height
            self._draw_text(start_x, y, line.upper())

    def _draw_text(self, x: float, y: float, text: str) -> None:
        cursor = x
        for char in text:
            segments = _FONT.get(char, _FONT.get("?", []))
            for (x1, y1), (x2, y2) in segments:
                vertices = [
                    vec2(cursor + x1 * self.scale, y + y1 * self.scale),
                    vec2(cursor + x2 * self.scale, y + y2 * self.scale),
                ]
                self.renderer._upload_vertices(vertices)
                self.renderer._draw(GL_LINES, 2)
            cursor += 7 * self.scale
